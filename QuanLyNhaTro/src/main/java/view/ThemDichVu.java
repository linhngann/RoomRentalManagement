    private void themDichVuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themDichVuButtonActionPerformed
        try {
                dichvu.setMaDichVu(maDichVuText.getText().trim());
                dichvu.setTenDichVu(tenDichVuText.getText().trim());
                dichvu.setGia(Double.parseDouble(giaDichVuText.getText().trim()));
                dichvu.setDonViTinh(donViTinhText.getText().trim());
                dichvu.setNgayBatDau(Date.valueOf(ngayBatDauText.getText().trim()));
                dichvu.setNgayKetThuc(Date.valueOf(ngayKetThucText.getText().trim()));
                 String status = "";
                if(dongYRadioButton.isSelected())
                {
                    status = "Y";
                }
                if(tuChoiRadioButton.isSelected())
                {
                    status = "N";
                }
                dichvu.setTrangThai(status);
                dichVuController.Insert(dichvu, ThemDichVu.this);

                this.dispose();
            } catch (NumberFormatException e) {
                // Handle number format exception for giaDichVuText
                javax.swing.JOptionPane.showMessageDialog(this, "Giá dịch vụ phải là một số.", "Lỗi nhập liệu", javax.swing.JOptionPane.ERROR_MESSAGE);
            } catch (IllegalArgumentException e) {
                // Handle illegal argument exception for date parsing
                javax.swing.JOptionPane.showMessageDialog(this, "Ngày không hợp lệ. Vui lòng nhập đúng định dạng yyyy-mm-dd.", "Lỗi nhập liệu", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_themDichVuButtonActionPerformed

